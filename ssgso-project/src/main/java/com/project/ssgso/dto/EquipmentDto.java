package com.project.ssgso.dto;

public class EquipmentDto {
	
	private int equ_no;
	private int ac_no;
	private String equ_val;
	
	public int getEqu_no() {
		return equ_no;
	}
	public void setEqu_no(int equ_no) {
		this.equ_no = equ_no;
	}
	public int getAc_no() {
		return ac_no;
	}
	public void setAc_no(int ac_no) {
		this.ac_no = ac_no;
	}
	public String getEqu_val() {
		return equ_val;
	}
	public void setEqu_val(String equ_val) {
		this.equ_val = equ_val;
	}
}
