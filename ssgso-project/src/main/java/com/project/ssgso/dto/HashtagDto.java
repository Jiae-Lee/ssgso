package com.project.ssgso.dto;

public class HashtagDto {
	private int hash_no;
	private int ac_no;
	private String hash_val;
	
	public HashtagDto() {}
	
	public int getHash_no() {
		return hash_no;
	}
	public void setHash_no(int hash_no) {
		this.hash_no = hash_no;
	}
	public int getAc_no() {
		return ac_no;
	}
	public void setAc_no(int ac_no) {
		this.ac_no = ac_no;
	}
	public String getHash_val() {
		return hash_val;
	}
	public void setHash_val(String hash_val) {
		this.hash_val = hash_val;
	}
	
	
}
